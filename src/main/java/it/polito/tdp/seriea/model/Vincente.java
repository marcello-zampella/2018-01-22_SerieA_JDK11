package it.polito.tdp.seriea.model;

public class Vincente {

	int anno;
	int punti;
	public Vincente(int anno, int valor) {
		super();
		this.anno = anno;
		this.punti = valor;
	}
	public int getAnno() {
		return anno;
	}
	public void setAnno(int anno) {
		this.anno = anno;
	}
	public int getPunti() {
		return punti;
	}
	public void setPunti(int punti) {
		this.punti = punti;
	};
	
	
	
}
